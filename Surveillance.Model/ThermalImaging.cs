        public int TopX { get; set; }


        /**
		 * 区域坐标y1
		 */
        public int TopY { get; set; }

        /**
		 * 区域坐标x2
		 */
        public int BottomX { get; set; }

        /**
		 * 区域坐标y2
		 */
        public int BottomY { get; set; }

        /**
		 * 区域ID
		 */
        public int AreaID { get; set; }

        /**
		 * 热成像时间
		 */
        public DateTime CreateTime { get; set; }

    }
}
